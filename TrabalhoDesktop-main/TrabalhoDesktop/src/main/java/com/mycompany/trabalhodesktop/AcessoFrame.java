    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
     
       
        String peca = tfPeca.getText();
        String quant = tfQuant.getText();
        String valorUn = tfValorUn.getText();
        
        
        if (peca.isEmpty() || peca.equals("") || quant.isEmpty() || quant.equals("") || valorUn.isEmpty() || valorUn.equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!", "Atenção!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (!quant.matches("[0-9]+")) {
            JOptionPane.showMessageDialog(null, "O campo QUANTIDADE deve conter apenas números!", "Atenção!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!valorUn.matches("[0-9]+")) {
            JOptionPane.showMessageDialog(null, "O campo VALOR UNITARIO deve conter apenas números!", "Atenção!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (!peca.matches("[a-zA-Z]+")){
           JOptionPane.showMessageDialog(null, "O campo PEÇA deve apenas conter letras!", "Atenção!", JOptionPane.ERROR_MESSAGE);
           return;
       }
        
        else{
            if (linhaSelecionada >= 0) {
            modelo.removeRow(linhaSelecionada);
            modelo.insertRow(linhaSelecionada, new Object[]{peca, quant, valorUn});
        } else {
            modelo.addRow(new Object[]{peca, quant, valorUn});
            JOptionPane.showMessageDialog(this, "SUCESSO!");
        }

        tfPeca.setText("");
        tfQuant.setText("");
        tfValorUn.setText("");
        tfPeca.requestFocus();
        linhaSelecionada = -1;
        
        double valor = Double.parseDouble(valorUn);
        int quantidade = Integer.parseInt(quant);
        
        valorTotal = valor * quantidade;
        
        
        
        Pecas pecas = new Pecas();
        pecas.setPeca(peca);
        pecas.setQuantidade(quantidade);
        pecas.setValorUn(valor);
        
        
        Controller.getInstance().salvarPecas(pecas);
        }
    }//GEN-LAST:event_btAdicionarActionPerformed

