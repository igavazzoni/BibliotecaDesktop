    private void btAdicionarServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarServicoActionPerformed
        String servico = tfServico.getText();
        String valor = tfValorServico.getText();
        
        if (servico.isEmpty() || servico.equals("") || valor.isEmpty() || valor.equals("")){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!", "Atenção!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (!valor.matches("[0-9]+")) {
            JOptionPane.showMessageDialog(null, "O campo VALOR SERVIÇO deve conter apenas números!", "Atenção!", JOptionPane.ERROR_MESSAGE);
            return;
        }
         
         if (!servico.matches("[a-zA-Z]+")){
           JOptionPane.showMessageDialog(null, "O campo SERVIÇO deve apenas conter letras!", "Atenção!", JOptionPane.ERROR_MESSAGE);
           return;
       }
        
        else{
            if (linhaSelecionada >= 0) {
            modelo.removeRow(linhaSelecionada);
            modelo.insertRow(linhaSelecionada, new Object[]{servico, valor});
        } else {
            modelo.addRow(new Object[]{servico, valor});
            JOptionPane.showMessageDialog(this, "SUCESSO!");
        }
            tfServico.setText("");
            tfValorServico.setText("");
            tfServico.requestFocus();
            linhaSelecionada = -1;
            
            double valorServico = Double.parseDouble(valor);
            valorTotalServico+= valorServico;
            
            Servico servicoo = new Servico();
            servicoo.setServico(servico);
            servicoo.setValor(valorServico);
            
            Controller.getInstance().salvarServico(servicoo);
        }
    }//GEN-LAST:event_btAdicionarServicoActionPerformed

